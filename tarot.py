def get_cards(card_numbers):
    cards = []
    for card_number in card_numbers:
        if not isinstance(card_number, int):
            raise ValueError("Input must be a list of integers")
        card = get_card_number(card_number)
        if card is not None:
            cards.append(card)
    return cards
